/* Precompute the (t+1)×n readout rows once; a path verdict is then a gather
   of 2(t+1) ints + one small Gaussian elimination. */
function makeRowTable(n, t) {
  const ctx = makeCtx32(n, t);
  const table = [];
  for (let i = 0; i <= t; i++) {
    const r = new Array(n);
    for (let j = 0; j < n; j++) r[j] = cellRow32(ctx, i, j);
    table.push(r);
  }
  return table;
}

function pathIsIS(table, n, t, col, scratch) {
  let k = 0;
  for (let i = 0; i <= t; i++) {
    scratch[k++] = table[i][col[i]];
    scratch[k++] = table[i][(col[i] + 1) % n];
  }
  // inline rank over n columns, early exit impossible (need full rank)
  let r = 0;
  for (let c = 0; c < n && r < k; c++) {
    const m = 1 << c;
    let p = -1;
    for (let q = r; q < k; q++) if (scratch[q] & m) { p = q; break; }
    if (p < 0) continue;
    const tmp = scratch[r]; scratch[r] = scratch[p]; scratch[p] = tmp;
    for (let q = 0; q < k; q++) if (q !== r && (scratch[q] & m)) scratch[q] ^= scratch[r];
    r++;
  }
  return r === n;
}

/* ========================================================================
   E1 — capacity path sweeps: n = 2(t+1), all paths with c(0) = 0.
   The Lean record stops at t = 4 (named probes). Here: exhaustive t ≤ 5
   (quick), t = 6 exhaustive + t = 7 sampled (all).
   ======================================================================== */
function capacitySweep(t, { sample = 0, seed = 1234 } = {}) {
  const n = 2 * (t + 1);
  const table = makeRowTable(n, t);
  const scratch = new Array(2 * (t + 1)).fill(0);
  const col = new Array(t + 1).fill(0);
  const byLast = {};                        // last-step ringDist → [decode, total]
  const byPair = {};                        // (d(t−1), d(t)) → [decode, total]
  let total = 0, decode = 0;
  const failExamples = [], wildDecodeExamples = [];
  const bump = (o, k, ok) => { (o[k] = o[k] || [0, 0]); o[k][1]++; if (ok) o[k][0]++; };

  const visit = () => {
    const ok = pathIsIS(table, n, t, col, scratch);
    total++; if (ok) decode++;
    const dLast = ringDist(col[t], col[t - 1], n);
    const dPrev = t >= 2 ? ringDist(col[t - 1], col[t - 2], n) : 0;
    bump(byLast, dLast, ok);
    bump(byPair, `${dPrev},${dLast}`, ok);
    const lip = (() => { for (let i = 0; i < t; i++) if (ringDist(col[i + 1], col[i], n) > 1) return false; return true; })();
    if (!ok && failExamples.length < 24) failExamples.push(col.slice());
    if (ok && !lip && dLast > 1 && wildDecodeExamples.length < 12) wildDecodeExamples.push(col.slice());
  };

  const t0 = Date.now();
  if (!sample) {
    // odometer over col[1..t]
    const idx = new Array(t).fill(0);
    for (;;) {
      for (let i = 0; i < t; i++) col[i + 1] = idx[i];
      visit();
      let d = 0;
      while (d < t && ++idx[d] === n) { idx[d] = 0; d++; }
      if (d === t) break;
    }
  } else {
    const rng = mulberry32(seed);
    for (let s = 0; s < sample; s++) {
      for (let i = 1; i <= t; i++) col[i] = Math.floor(rng() * n);
      visit();
    }
  }
  const ms = Date.now() - t0;
  // last-step-Lipschitz predicate confusion
  let lastTP = 0, lastFP = 0, lastFN = 0, lastTN = 0;
  for (const [d, [dec, tot]] of Object.entries(byLast)) {
    if (+d <= 1) { lastTP += dec; lastFP += tot - dec; } else { lastFN += dec; lastTN += tot - dec; }
  }
  return {
    n, t, mode: sample ? `sampled ${sample}` : 'exhaustive', total, decode,
    fraction: decode / total, ms,
    byLast, byPair, failExamples, wildDecodeExamples,
    lastStepPredicate: {
      note: 'H_last = "ringDist(c(t−1), c(t)) ≤ 1 predicts decode" — exact at t=2 (pairScreen_class_6_2)',
      agreeWhenLip: lastTP, disagreeWhenLip: lastFP, decodeDespiteWild: lastFN, failWhenWild: lastTN,
      exact: lastFP === 0 && lastFN === 0,
    },
  };
}

/* ========================================================================
   E2 — the slack ladder: how much horizon beyond capacity does the WILD
   regime need before every path decodes?  For n fixed, sweep t upward from
   the capacity horizon; record the failing fraction at each slack.
   ======================================================================== */
function slackLadder(n, tMax, capPerT = 3_000_000, seed = 77) {
  const out = [];
  for (let t = Math.ceil(n / 2 - 1); t <= tMax; t++) {
    if (n > 2 * (t + 1)) continue;                    // below capacity: nothing decodes anyway
    const totalPaths = n ** t;
    const table = makeRowTable(n, t);
    const scratch = new Array(2 * (t + 1)).fill(0);
    const col = new Array(t + 1).fill(0);
    let total = 0, decode = 0;
    const t0 = Date.now();
    if (totalPaths <= capPerT) {
      const idx = new Array(t).fill(0);
      for (;;) {
        for (let i = 0; i < t; i++) col[i + 1] = idx[i];
        total++; if (pathIsIS(table, n, t, col, scratch)) decode++;
        let d = 0;
        while (d < t && ++idx[d] === n) { idx[d] = 0; d++; }
        if (d === t) break;
      }
    } else {
      const rng = mulberry32(seed + t);
      const N = capPerT;
      for (let s = 0; s < N; s++) {
        for (let i = 1; i <= t; i++) col[i] = Math.floor(rng() * n);
        total++; if (pathIsIS(table, n, t, col, scratch)) decode++;
      }
    }
    out.push({ n, t, slack: 2 * (t + 1) - n, total, decode, fraction: decode / total,
               exhaustive: totalPaths <= capPerT, ms: Date.now() - t0 });
    if (decode === total && totalPaths <= capPerT) break;   // universality reached, exactly
  }
  return out;
}

/* ========================================================================
   E3 — structured wild families across t (exact, tiny):
   slope-2 line, late jump k, kink (single step-2 at position m).
   ======================================================================== */
function familySweep(tMax = 10) {
  const rows = [];
  for (let t = 2; t <= tMax; t++) {
    const n = 2 * (t + 1);
    const ctx = makeCtx32(n, t);
    const verdict = col => analyzeScreen(n, t, pairScreen(n, t, col), ctx).isInformationSet;
    // slope-2 line
    const line = Array.from({ length: t + 1 }, (_, i) => mod(2 * i, n));
    // late jump 2 (…0,0,2) and early jump 2 (…0,2,2,…): kink position sweep
    const kinks = [];
    for (let m = 1; m <= t; m++) {                      // step of +2 between m−1 and m
      const col = new Array(t + 1).fill(0);
      for (let i = m; i <= t; i++) col[i] = 2;
      kinks.push(verdict(col) ? 1 : 0);
    }
    // teleport n/2 zigzag
    const tele = Array.from({ length: t + 1 }, (_, i) => (i % 2 ? n / 2 : 0));
    rows.push({
      t, n,
      slope2: verdict(line),
      teleportHalf: verdict(tele),
      kinkByPosition: kinks,                            // index m−1 ⇒ jump between rows m−1, m
      kinkNote: '1 = decodes; position t is the "late jump" (fails at t=2,4 per the Lean probes)',
    });
  }
  return rows;
}

/* ========================================================================
   E4 — random-subset Monte Carlo: P(size-n subset is an information set),
   kernel-dimension distribution, and the local/global split (closure
   completeness among information sets). Baseline: ∏(1−2^{−k}) ≈ 0.288788.
   ======================================================================== */
function subsetMonteCarlo(configs, samples = 30000, seed = 5) {
  const out = [];
  for (const { n, t, sizes } of configs) {
    const ctx = makeCtx32(n, t);
    const rng = mulberry32(seed + n * 31 + t);
    for (const size of sizes) {
      let isCount = 0, closureComplete = 0, closurePartialIS = 0;
      const kerHist = {};
      const t0 = Date.now();
      for (let s = 0; s < samples; s++) {
        const cells = [], seen = new Set();
        while (cells.length < size) {
          const i = Math.floor(rng() * (t + 1)), j = Math.floor(rng() * n);
          const k = i * n + j;
          if (!seen.has(k)) { seen.add(k); cells.push({ i, j }); }
        }
        const M = cells.map(c => cellRow32(ctx, c.i, c.j));
        const ker = kernel32(M, n).length;
        kerHist[ker] = (kerHist[ker] || 0) + 1;
        if (ker === 0) {
          isCount++;
          if (s % 10 === 0) {                            // closure is pricier: sample 10%
            const cl = closure(n, t, cells);
            cl.complete ? closureComplete++ : closurePartialIS++;
          }
        }
      }
      out.push({
        n, t, size, samples, isCount, pIS: isCount / samples,
        baselineRandomMatrix: randomMatrixInvertibleProb(n),
        kernelDimHistogram: kerHist,
        closureSampled: { complete: closureComplete, partial: closurePartialIS,
          note: 'among information sets (10% subsample): complete = locally decodable, partial = pinned only by global algebra' },
        ms: Date.now() - t0,
      });
      log(`  E4 (${n},${t}) size ${size}: P(IS) = ${(isCount / samples).toFixed(4)} vs baseline ${randomMatrixInvertibleProb(n).toFixed(4)}`);
    }
  }
  return out;
}

/* ========================================================================
   E5 — exhaustive tight-subset census: ALL subsets of size n at (6,2)
   [C(18,6) = 18 564] and optionally (8,3) [C(32,8) = 10 518 300].
   The exact fraction of minimal information sets — ground truth for the
   arbitrary-subset classification at small size.
   ======================================================================== */
function tightCensus(n, t) {
  const total = (t + 1) * n;
  const ctx = makeCtx32(n, t);
  const rowsFlat = [];
  for (let i = 0; i <= t; i++) for (let j = 0; j < n; j++) rowsFlat.push(cellRow32(ctx, i, j));
  const idx = Array.from({ length: n }, (_, k) => k);   // combination odometer
  const scratch = new Array(n).fill(0);
  let count = 0, isCount = 0;
  const t0 = Date.now();
  for (;;) {
    let r = 0, k = n;
    for (let q = 0; q < n; q++) scratch[q] = rowsFlat[idx[q]];
    for (let c = 0; c < n && r < k; c++) {
      const m = 1 << c;
      let p = -1;
      for (let q = r; q < k; q++) if (scratch[q] & m) { p = q; break; }
      if (p < 0) continue;
      const tmp = scratch[r]; scratch[r] = scratch[p]; scratch[p] = tmp;
      for (let q = r + 1; q < k; q++) if (scratch[q] & m) scratch[q] ^= scratch[r];
      r++;
    }
    count++;
    if (r === n) isCount++;
    // next combination
    let i = n - 1;
    while (i >= 0 && idx[i] === total - n + i) i--;
    if (i < 0) break;
    idx[i]++;
    for (let q = i + 1; q < n; q++) idx[q] = idx[q - 1] + 1;
  }
  return { n, t, cells: total, subsets: count, informationSets: isCount,
           fraction: isCount / count, ms: Date.now() - t0 };
}

/* ========================================================================
   E6 — the shadow atlas across (n, t): the maximal ghost zero-sets ARE the
   arbitrary-subset classification. Count them, orbit-reduce them, keep the
   masks for browser display at small n.
   ======================================================================== */
function shadowAtlas(nMax = 14) {
  const out = [];
  for (let n = 3; n <= nMax; n++) {
    const t = Math.max(0, Math.ceil(n / 2) - 1);        // capacity horizon
    const t0 = Date.now();
    const atlas = maximalShadows(n, t);
    // orbit reduction under translation (j → j+1) and reflection (j → −j)
    const orbits = new Map();
    for (const M of atlas.maximal) {
      let best = null;
      for (let s = 0; s < n; s++) for (const refl of [false, true]) {
        const key = [];
        for (let i = 0; i <= t; i++) {
          let rowBits = 0;
          for (let j = 0; j < n; j++) {
            const jj = refl ? mod(-(j + s), n) : mod(j + s, n);
            const k = i * n + jj;
            if ((M.mask[k >>> 5] >>> (k & 31)) & 1) rowBits |= (1 << j);
          }
          key.push(rowBits);
        }
        const ks = key.join(',');
        if (best === null || ks < best) best = ks;
      }
      orbits.set(best, (orbits.get(best) || 0) + 1);
    }
    const entry = {
      n, t, maximalShadows: atlas.maximal.length,
      distinctZeroSets: atlas.distinctZeroSets,
      orbitClasses: orbits.size,
      shadowSizes: atlas.maximal.map(M => M.size).sort((a, b) => b - a),
      totalCells: atlas.totalCells,
      ms: Date.now() - t0,
    };
    if (n <= 12) entry.masks = atlas.maximal.map(M => ({ seed: M.seed, mask: Array.from(M.mask) }));
    out.push(entry);
    log(`  E6 (${n},${t}): ${entry.maximalShadows} maximal shadows, ${entry.orbitClasses} orbit classes (${entry.ms} ms)`);
  }
  return out;
}

/* ========================================================================
   E7 — Lipschitz surjectivity probe (T31 generalization candidate):
   is the pathScreen readout surjective for EVERY 1-Lipschitz path, i.e.
   rank = min(n, 2(t+1))?  Random paths across the (n,t) grid.
   ======================================================================== */
function lipschitzSurjectivity(trials = 20000, seed = 99) {
  const rng = mulberry32(seed);
  let checked = 0, violations = [];
  for (let s = 0; s < trials; s++) {
    const n = 2 + Math.floor(rng() * 27);               // ≤ 29 int path
    const t = Math.floor(rng() * 14);
    const col = [Math.floor(rng() * n)];
    for (let i = 0; i < t; i++) col.push(mod(col[i] + (Math.floor(rng() * 3) - 1), n));
    const a = analyzeScreen(n, t, pairScreen(n, t, col));
    const want = Math.min(n, a.bits);                   // bits = |cells| (≤ 2(t+1), = unless overlap)
    if (a.rank !== want) { violations.push({ n, t, col, rank: a.rank, bits: a.bits }); if (violations.length > 5) break; }
    checked++;
  }
  return { trials: checked, violations,
    conjecture: 'pathScreen readout has full rank min(n, |cells|) for every 1-Lipschitz worldline (T31 generalization candidate)',
    holds: violations.length === 0 };
}

/* ========================================================================
   E8 — the permanent ghost: teleport-h worldline (0, h, 0, h, …), h = n/2.
   Empirical law: h odd (n ≡ 2 mod 4) → NEVER decodes, kernel dim h−1,
   at every horizon tried; h even → decodes exactly at capacity t = n/2 − 1.
   At n = 6 the ghost is the period-2 seed δ₂+δ₄ locked in antiphase with
   the observer (verified mechanism); the general mechanism is open.
   ======================================================================== */
function teleportLaw(nMax = 26, tProbe = 24) {
  const out = [];
  for (let n = 6; n <= nMax; n += 2) {
    const h = n / 2;
    let firstIS = null, kernelDim = null, kernelAtDeep = null;
    for (let t = Math.ceil(n / 2) - 1; t <= tProbe; t++) {
      const col = Array.from({ length: t + 1 }, (_, i) => (i % 2 ? h : 0));
      const a = analyzeScreen(n, t, pairScreen(n, t, col));
      if (a.isInformationSet) { firstIS = t; break; }
      kernelDim = a.kernel.length;
      if (t === tProbe) kernelAtDeep = a.kernel.slice(0, 4);
    }
    out.push({ n, h, hParity: h % 2 ? 'odd' : 'even',
      verdict: firstIS === null ? `never (t ≤ ${tProbe})` : `decodes at t=${firstIS}`,
      firstIS, kernelDimWhenDark: firstIS === null ? kernelDim : null,
      ghostExamples: kernelAtDeep });
  }
  return out;
}

/* ========================================================================
   E9 — the shadow laws, verified precisely (the atlas patterns as claims):
   C1 even n ≥ threshold: maximal shadows ⟺ single-parity-class seeds
   C2 odd n ≥ threshold: ALL zero-sets pairwise incomparable
   ======================================================================== */
function shadowLaws() {
  const isParitySeed = (seed, n) => {
    let evenMask = 0; for (let j = 0; j < n; j += 2) evenMask |= (1 << j);
    const oddMask = (evenMask << 1) & ((1 << n) - 1);
    return (seed & ~evenMask) === 0 || (seed & ~oddMask) === 0;
  };
  const rows = [];
  for (let n = 3; n <= 14; n++) {
    for (const t of [Math.ceil(n / 2) - 1, n, 2 * n]) {
      if (n > 2 * (t + 1)) continue;
      const atlas = maximalShadows(n, t);
      const entry = { n, t, maximal: atlas.maximal.length, distinct: atlas.distinctZeroSets };
      if (n % 2 === 0) {
        entry.parityReps = atlas.maximal.filter(M => isParitySeed(M.seed, n)).length;
        entry.C1 = entry.parityReps === atlas.maximal.length && atlas.maximal.length === 2 * (2 ** (n / 2) - 1);
      } else {
        entry.C2 = atlas.maximal.length === (1 << n) - 1 && atlas.distinctZeroSets === (1 << n) - 1;
      }
      rows.push(entry);
    }
  }
  return {
    rows,
    C1: 'even n ≤ 2(t+1): the maximal ghost shadows are EXACTLY the zero-sets of the 2^{n/2+1}−2 nonzero single-parity-class seeds (containment half has a 3-line parity-splitting proof: traj(z_e) and traj(z_o) never overlap, so Z(z) ⊆ Z(z_e) and Z(z_o))',
    C2: 'odd n ≤ 2(t+1): all 2^n−1 ghost zero-sets are pairwise incomparable — Z(z) ⊆ Z(z\') ⟹ z = z\'; the classification does not compress',
    C1holds: null, C2holds: null,
  };
}

/* ========================================================================
   E10 — power-of-two universality (C3): on n = 2^k at capacity t = n/2−1,
   every worldline screen appears to decode — 𝔽₂[x]/(xⁿ−1) is LOCAL there
   (u = 1+x nilpotent), and the adjacent pair contributes u-valuations
   {2i, 2i+1} at every level regardless of the path (proof sketch in
   FINDINGS.md). Sampled here at n = 16, 32, 64 via the bitset path.
   ======================================================================== */
async function powerOfTwo() {
  const { makeCtxBS, analyzeScreen: az, pairScreen: ps } = await import('../js/lib/rule90.js');
  const out = [];
  for (const [n, samples] of [[16, 200000], [32, 40000], [64, 4000]]) {
    const t = n / 2 - 1;
    const ctx = makeCtxBS(n, t);
    const rng = mulberry32(4242 + n);
    let dec = 0;
    const t0 = Date.now();
    for (let s = 0; s < samples; s++) {
      const col = [0];
      for (let i = 0; i < t; i++) col.push(Math.floor(rng() * n));
      if (az(n, t, ps(n, t, col), ctx).isInformationSet) dec++;
    }
    out.push({ n, t, samples, decode: dec, fraction: dec / samples, ms: Date.now() - t0 });
    log(`  E10 n=${n} t=${t}: ${dec}/${samples} = ${(100 * dec / samples).toFixed(3)}%`);
  }
  return {
    rows: out,
    conjecture: 'C3: on n = 2^k at capacity, EVERY adjacent-pair worldline decodes (u-adic valuation argument; 𝔽₂[x]/(xⁿ−1) local). Exhaustive at n=8 (Lean, all 512); zero failures in every sample here.',
  };
}

